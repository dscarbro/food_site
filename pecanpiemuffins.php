<div id="howtocookppm">
<p>Preheat oven to 350 degrees F (175 degrees C). Grease and flour 18 mini muffin cups or line with paper muffin liners.</p>

<p>In a medium bowl, stir together brown sugar, flour and pecans. In a separate bowl beat the butter and eggs together until smooth, stir into the dry ingredients just until combined. Spoon the batter into the prepared muffin cups. Cups should be about 2/3 full.<p>

<p>Bake at 350 degrees F (175 degrees C) for 20 to 25 minutes. Cool on wire racks when done.</p>
